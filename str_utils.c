#include "minishell.h"

size_t	ft_strlen(const char *s, char c)
{
	size_t	ln;

	ln = 0;
	while (s[ln] != c)
		ln++;
	return (ln);
}

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t	i;

	i = 0;
	if (n == 0)
		return (0);
	while (s1[i] && s2[i] && s1[i] == s2[i] && i < n - 1)
		i++;
	return ((unsigned char) s1[i] - (unsigned char) s2[i]);
}

int	is_space(char c)
{
	return ((c >= 9 && c <= 13) || c == 32);
}
